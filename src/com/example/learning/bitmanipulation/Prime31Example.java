package com.example.learning.bitmanipulation;

public class Prime31Example {
	
	public static void main(String[] args) {
		
		System.out.println(31 * 1);
		
		System.out.println((1 << 5) - 1); // 2 ^ 5
		
		System.out.println(Integer.toBinaryString(31));
		System.out.println(Long.toBinaryString(31));
	}

}
